class Employee:

#modifiers
    def setName(self, name):
        self.name = name
    
    def setAge(self, age):
        self.age = age

    def setSalary(self, salary):
        self.salary = salary

#accessors
    def getName(self):
        return self.name
    
    def getAge(self):
        return self.age

    def getSalary(self):
        return self.salary


        